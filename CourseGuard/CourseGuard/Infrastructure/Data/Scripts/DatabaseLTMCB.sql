-- 2. TẠO CÁC BẢNG (THEO THỨ TỰ CHA TRƯỚC - CON SAU)
-- =============================================

-- Cấp 1: Các bảng danh mục độc lập
CREATE TABLE ROLES (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(50) NOT NULL UNIQUE,
    DESCRIPTION NVARCHAR(255)
);

CREATE TABLE PERMISSIONS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    CODE NVARCHAR(100) NOT NULL UNIQUE,
    NAME NVARCHAR(100),
    DESCRIPTION NVARCHAR(255),
    IS_ACTIVE BIT DEFAULT 1
);

-- Cấp 2: Bảng liên kết Role-Permission và Bảng Users (phụ thuộc ROLES)
CREATE TABLE ROLE_PERMISSIONS (
    ROLE_ID INT NOT NULL,
    PERMISSION_ID INT NOT NULL,
    PRIMARY KEY (ROLE_ID, PERMISSION_ID),
    FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ID) ON DELETE CASCADE,
    FOREIGN KEY (PERMISSION_ID) REFERENCES PERMISSIONS(ID) ON DELETE CASCADE
);

CREATE TABLE USERS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    USERNAME NVARCHAR(50) NOT NULL UNIQUE,
    PASSWORD_HASH NVARCHAR(255) NOT NULL,
    FULL_NAME NVARCHAR(100),
    EMAIL NVARCHAR(100) UNIQUE,
    ROLE_ID INT NOT NULL,
    STATUS NVARCHAR(20) DEFAULT 'ACTIVE',
    CREATED_AT DATETIME DEFAULT GETDATE(),
    CONSTRAINT FK_USERS_ROLES FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ID)
);

-- Cấp 3: Khóa học (phụ thuộc USERS - Teacher)
CREATE TABLE COURSES (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    NAME NVARCHAR(200) NOT NULL,
    DESCRIPTION NVARCHAR(MAX),
    TEACHER_ID INT NOT NULL,
    STATUS NVARCHAR(20) DEFAULT 'ACTIVE',
    START_DATE DATE,
    END_DATE DATE,
    CREATED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (TEACHER_ID) REFERENCES USERS(ID)
);

-- Cấp 4: Các bảng phụ thuộc vào Khóa học và Users
CREATE TABLE ENROLLMENTS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    STUDENT_ID INT NOT NULL,
    STATUS NVARCHAR(20) DEFAULT 'PENDING',
    JOINED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (STUDENT_ID) REFERENCES USERS(ID)
);

CREATE TABLE EXAMS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    TITLE NVARCHAR(200),
    OPEN_TIME DATETIME,
    CLOSE_TIME DATETIME,
    DURATION_MINUTES INT,
    MAX_ATTEMPTS INT DEFAULT 1,
    ALLOW_REVIEW BIT DEFAULT 0,
    SHUFFLE_QUESTIONS BIT DEFAULT 0,
    CREATED_BY INT,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (CREATED_BY) REFERENCES USERS(ID)
);

CREATE TABLE QUESTIONS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    CONTENT NVARCHAR(MAX),
    TYPE NVARCHAR(20),
    CORRECT_ANSWER NVARCHAR(MAX),
    SCORE DECIMAL(5,2) DEFAULT 1,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE
);

-- Cấp 5: Các bảng phụ thuộc EXAMS và QUESTIONS
CREATE TABLE EXAM_QUESTIONS (
    EXAM_ID INT NOT NULL,
    QUESTION_ID INT NOT NULL,
    PRIMARY KEY (EXAM_ID, QUESTION_ID),
    FOREIGN KEY (EXAM_ID) REFERENCES EXAMS(ID) ON DELETE CASCADE,
    FOREIGN KEY (QUESTION_ID) REFERENCES QUESTIONS(ID)
);

CREATE TABLE EXAM_ATTEMPTS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    EXAM_ID INT NOT NULL,
    STUDENT_ID INT NOT NULL,
    START_TIME DATETIME DEFAULT GETDATE(),
    SUBMIT_TIME DATETIME,
    SCORE DECIMAL(5,2),
    STATUS NVARCHAR(20) DEFAULT 'IN_PROGRESS',
    FOREIGN KEY (EXAM_ID) REFERENCES EXAMS(ID) ON DELETE CASCADE,
    FOREIGN KEY (STUDENT_ID) REFERENCES USERS(ID)
);

-- Cấp 6: Answers và Vi phạm (phụ thuộc Attempts)
CREATE TABLE ANSWERS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    ATTEMPT_ID INT NOT NULL,
    QUESTION_ID INT NOT NULL,
    ANSWER_CONTENT NVARCHAR(MAX),
    SCORE DECIMAL(5,2),
    FOREIGN KEY (ATTEMPT_ID) REFERENCES EXAM_ATTEMPTS(ID) ON DELETE CASCADE,
    FOREIGN KEY (QUESTION_ID) REFERENCES QUESTIONS(ID)
);

CREATE TABLE VIOLATIONS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID INT NOT NULL,
    EXAM_ATTEMPT_ID INT,
    TYPE NVARCHAR(100),
    CREATED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    FOREIGN KEY (EXAM_ATTEMPT_ID) REFERENCES EXAM_ATTEMPTS(ID)
);

-- Cấp 7: Các bảng tiện ích khác (Chat, Materials, Notifications...)
CREATE TABLE ONLINE_SESSIONS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    TITLE NVARCHAR(200),
    START_TIME DATETIME,
    END_TIME DATETIME,
    MEETING_LINK NVARCHAR(500),
    CREATED_BY INT,
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (CREATED_BY) REFERENCES USERS(ID)
);

CREATE TABLE MATERIALS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    FILE_NAME NVARCHAR(255),
    FILE_PATH NVARCHAR(500),
    UPLOADED_BY INT,
    UPLOADED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (UPLOADED_BY) REFERENCES USERS(ID)
);

CREATE TABLE MESSAGES (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    COURSE_ID INT NOT NULL,
    SENDER_ID INT NOT NULL,
    CONTENT NVARCHAR(MAX),
    SENT_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (COURSE_ID) REFERENCES COURSES(ID) ON DELETE CASCADE,
    FOREIGN KEY (SENDER_ID) REFERENCES USERS(ID)
);

CREATE TABLE NOTIFICATIONS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID INT NOT NULL,
    TITLE NVARCHAR(200),
    CONTENT NVARCHAR(MAX),
    IS_READ BIT DEFAULT 0,
    CREATED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

CREATE TABLE DEVICES (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID INT NOT NULL,
    DEVICE_NAME NVARCHAR(200),
    IP_ADDRESS NVARCHAR(50),
    STATUS NVARCHAR(20) DEFAULT 'ACTIVE',
    LAST_ACTIVE DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

CREATE TABLE AUDIT_LOGS (
    ID INT IDENTITY(1,1) PRIMARY KEY,
    USER_ID INT,
    ACTION NVARCHAR(200),
    TARGET_TABLE NVARCHAR(100),
    TARGET_ID INT,
    CREATED_AT DATETIME DEFAULT GETDATE(),
    FOREIGN KEY (USER_ID) REFERENCES USERS(ID)
);
GO

